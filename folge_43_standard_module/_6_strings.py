# 6. string – Bietet nützliche Konstanten für Zeichenketten
import string

char_str = string.ascii_letters
print(char_str)
char_str = string.ascii_lowercase
print(char_str)
char_str = string.ascii_uppercase
print(char_str)

ziffern = string.digits
print(ziffern)

symbole = string.punctuation
print(symbole)
